def pick_fantasy_name(user_nums: list[int], seed: float, geom_idx: int) -> str:
    payload = f"name|{','.join(map(str, sorted(user_nums)))}|{seed:.8f}|{geom_idx}"
    digest = hashlib.sha256(payload.encode()).hexdigest()
    idx = int(digest[:6], 16) % len(FANTASY_CONSTELLATION_NAMES)
    return FANTASY_CONSTELLATION_NAMES[idx]
